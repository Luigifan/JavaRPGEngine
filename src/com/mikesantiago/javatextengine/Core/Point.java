package com.mikesantiago.javatextengine.Core;

public class Point
{
	private float _x, _y;
	
	public Point(float x, float y)
	{
		_x = x;
		_y = y;
	}

	public float getX() {
		return _x;
	}

	public void setX(float _x) {
		this._x = _x;
	}

	public float getY() {
		return _y;
	}

	public void setY(float _y) {
		this._y = _y;
	}
	
	
}